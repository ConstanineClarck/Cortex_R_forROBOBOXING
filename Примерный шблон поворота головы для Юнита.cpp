#include <iostream>
#include <robot_head_library.h> // Замените на библиотеку, соответствующую вашему оборудованию

int main() {
    // Инициализация микроконтроллера и двигателей головы
    if (!initialize_robot_head()) {
        std::cerr << "Ошибка инициализации головы робота!" << std::endl;
        return 1;
    }

    // Задаем начальное положение головы
    set_head_position(0, 0); // Угол в градусах для панорамирования и тильтирования

    // Просим пользователя ввести угол панорамирования и тильтирования
    double pan_angle, tilt_angle;
    std::cout << "Введите угол панорамирования (в градусах): ";
    std::cin >> pan_angle;
    std::cout << "Введите угол тильтирования (в градусах): ";
    std::cin >> tilt_angle;

    // Перемещаем голову робота в заданное положение
    if (!move_head(pan_angle, tilt_angle)) {
        std::cerr << "Ошибка перемещения головы робота!" << std::endl;
        return 1;
    }

    // Завершение работы и освобождение ресурсов
    cleanup_robot_head();

    return 0;
}
